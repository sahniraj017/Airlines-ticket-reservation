    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DateFormat da=new SimpleDateFormat("yyyy-MM-dd");
        String date=da.format(txtdate.getDate());
        String ticketid=txtticketno.getText();
        String flightid=flightno.getText();
        String custid=txtcustid.getText();
        String flightclass=txtclass.getSelectedItem().toString().trim();
        String price=txtprice.getText();
        String seats=txtseats.getValue().toString();
        
         String regex = "^[1-9]\\d*$";
            Pattern pN = Pattern.compile(regex); 
            Matcher Nmatch= pN.matcher(seats);
            
             Date today= new Date();
        
            if(date.isBlank() || ticketid.isEmpty()|| flightid.isEmpty()||custid.isEmpty()||flightclass.isEmpty()||price.isEmpty()||seats.isEmpty()){JOptionPane.showMessageDialog(null, "Fields should not be blank");}
           
            
            else if (!Nmatch.matches()) { JOptionPane.showMessageDialog(null, "Input valid Seat number");}
            
            else if(txtdate.getDate().compareTo(today)<0){JOptionPane.showMessageDialog(null, "Invalid date");}
   
            else{
            try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline1","root","sahniraj017");
            pst =con.prepareStatement("insert into ticket(id,flightid,custid,class,price,seats,date)values(?,?,?,?,?,?,?)");
            pst.setString(1, ticketid);
            pst.setString(2, flightid);
            pst.setString(3,custid);
            pst.setString(4, flightclass);
            pst.setString(5, price);
            pst.setString(6, seats);
            pst.setString(7,date);
        
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Ticket Booked");
                
            } catch (SQLException ex) {
                Logger.getLogger(ticket_booking.class.getName()).log(Level.SEVERE, "Date must not be null", ex);
            }
           }
    }//GEN-LAST:event_jButton3ActionPerformed

