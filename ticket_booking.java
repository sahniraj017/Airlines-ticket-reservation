    public void autoID()
    {
        try
        {
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/airline1","root","sahniraj017");
            Statement s=con.createStatement();
            ResultSet rs = s.executeQuery("Select MAX(id) from ticket");
            rs.next();
            rs.getString("MAX(id)");
            if(rs.getString("MAX(id)")==null)
            {
                txtticketno.setText("T001");
            }
            else
            {
                long id=Long.parseLong(rs.getString("MAX(id)").substring(2,rs.getString("MAX(id)").length()));
                id++;
                txtticketno.setText("T"+ String.format("%03d", id));
            }
        }
         catch(SQLException ex){
             Logger.getLogger(ticket_booking.class.getName()).log(Level.SEVERE, null,ex);
        }
    }
